// Function to add two numbers
function sum(num1, num2) {
    return num1 + num2;
}

// Test the function with some numbers
const result1 = sum(5, 3);
console.log("Sum of 5 and 3 is:", result1);

const result2 = sum(10, 20);
console.log("Sum of 10 and 20 is:", result2);

// You can also use the function directly in console.log
console.log("Sum of 7 and 8 is:", sum(7, 8)); 