// Basic for loop to print numbers 1 through 10
console.log("Printing numbers 1 through 10:");
for (let i = 1; i <= 10; i++) {
    console.log(i);
}

// Additional example: Print even numbers from 2 to 10
console.log("\nPrinting even numbers 2 through 10:");
for (let i = 2; i <= 10; i += 2) {
    console.log(i);
}

// Additional example: Print numbers in reverse from 10 to 1
console.log("\nPrinting numbers in reverse from 10 to 1:");
for (let i = 10; i >= 1; i--) {
    console.log(i);
} 