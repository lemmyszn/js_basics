// String variable
const name = "John Doe";
console.log("Name:", name);
console.log("Type of name:", typeof name);

// Number variable
const age = 25;
console.log("Age:", age);
console.log("Type of age:", typeof age);

// Boolean variable
const isStudent = true;
console.log("Is Student:", isStudent);
console.log("Type of isStudent:", typeof isStudent);

// Object variable
const address = {
    city: "New York",
    country: "USA"
};
console.log("Address:", address);
console.log("Type of address:", typeof address);

// Array variable
const hobbies = ["reading", "coding", "hiking", "photography"];
console.log("Hobbies:", hobbies);
console.log("Type of hobbies:", typeof hobbies);

// Undefined variable
let undefinedVariable;
console.log("Undefined Variable:", undefinedVariable);
console.log("Type of undefinedVariable:", typeof undefinedVariable);

// Null variable
const nullVariable = null;
console.log("Null Variable:", nullVariable);
console.log("Type of nullVariable:", typeof nullVariable);